from pathlib import Path

def calculate_file_identifier(file_path: Path) -> str:
    """Вычисляет уникальный идентификатор файла на основе его метаданных.
    
    Идентификатор формируется из размера файла (в байтах) и времени последней модификации,
    что позволяет эффективно сравнивать файлы по их содержимому без непосредственного чтения.
    Формат идентификатора: "размер-время_модификации".

    Args:
        file_path (Path): Абсолютный путь к файлу для анализа. Должен существовать и быть доступным для чтения.

    Returns:
        str: Строковый идентификатор в формате "размер-время_модификации" (например, "1024-1680000000.0").
             Возвращает пустую строку, если файл не существует или недоступен для чтения.

    Raises:
        OSError: Неявно возникает при проблемах доступа к файлу, но перехватывается внутри функции.
    """
    try:
        stat = file_path.stat()
        return f"{stat.st_size}-{stat.st_mtime}"
    except OSError:
        return ""
    